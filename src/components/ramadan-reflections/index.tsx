// دالة للحصول على تأمل عشوائي
export function getRandomReflection(): Reflection {
  const randomIndex = Math.floor(Math.random() * ramadanReflections.length);
  return ramadanReflections[randomIndex];
}

// دالة للحصول على تأمل حسب اليوم
export function getReflectionByDay(day: number): Reflection {
  const index = (day - 1) % ramadanReflections.length;
  return ramadanReflections[index];
}

// دالة للحصول على تأملات حسب الفئة
export function getReflectionsByCategory(category: string): Reflection[] {
  return ramadanReflections.filter(r => r.category === category);
}
